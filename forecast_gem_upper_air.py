def _extract_points_grib(grib_bytes, target_lats, target_lons, var_name):
    """
    Write GRIB2 bytes to a temp file, open with xarray+cfgrib engine, extract
    nearest-neighbour values for every (target_lat, target_lon) pair, then
    delete the temp file.

    Grid handling:
      GDPS LatLon0.15  → regular grid → 1-D lat/lon coords → vectorised argmin
      RDPS RLatLon0.09 → rotated grid → 2-D lat/lon coords → brute-force argmin

    AirTemp is stored in K; we convert to °C here.
    """
    fd, tmp_path = tempfile.mkstemp(suffix='.grib2')
    os.close(fd)  # release fd; write via path so file persists after close
    try:
        with open(tmp_path, 'wb') as f:
            f.write(grib_bytes)

        try:
            ds = xr.open_dataset(tmp_path, engine='cfgrib',
                                 backend_kwargs={'errors': 'ignore'})
        except Exception:
            ds = xr.open_dataset(tmp_path, engine='cfgrib')

        da = ds[list(ds.data_vars)[0]]
        da.load()  # eager read into RAM before temp file is deleted

    finally:
        if os.path.exists(tmp_path):
            os.unlink(tmp_path)

    # Identify coordinate names — cfgrib uses 'latitude'/'longitude' for
    # regular grids and may expose 2-D arrays for rotated grids
    lat_name = next((c for c in ('latitude', 'lat', 'y') if c in da.coords), None)
    lon_name = next((c for c in ('longitude', 'lon', 'x') if c in da.coords), None)

    lats = da[lat_name].values
    lons = da[lon_name].values
    lons = ((lons + 180) % 360) - 180   # normalise to [-180, 180)

    units   = da.attrs.get('GRIB_units', '') or da.attrs.get('units', '')
    is_temp = 'Temp' in var_name
    data    = da.values

    target_lats = np.asarray(target_lats)
    target_lons = np.asarray(target_lons)

    if lats.ndim == 2:
        # Rotated lat-lon (RDPS) — KDTree batch nearest-neighbour
        tree     = cKDTree(np.column_stack([lats.ravel(), lons.ravel()]))
        _, fidx  = tree.query(np.column_stack([target_lats, target_lons]))
        iy, ix   = np.unravel_index(fidx, lats.shape)
        vals     = data[iy, ix].astype(float)
    else:
        # Regular lat-lon (GDPS) — fully vectorised
        ilats = np.argmin(np.abs(lats[:, None] - target_lats), axis=0)
        ilons = np.argmin(np.abs(lons[:, None] - target_lons), axis=0)
        vals  = data[ilats, ilons].astype(float)

    # Apply temp conversion and validity mask in one pass
    if is_temp and (units == 'K' or np.nanmax(vals) > 100):
        vals = vals - 273.15

    result = {}
    for (tlat, tlon), val in zip(zip(target_lats, target_lons), vals):
        if _math.isnan(val) or abs(val) > 99000:
            result[(tlat, tlon)] = None
        else:
            result[(tlat, tlon)] = round(float(val), 2) if is_temp else float(val)

    return result


