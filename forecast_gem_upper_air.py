def _latest_run(run_hours, min_age_h=1.5):
    now = datetime.now(_tz.utc)
    for h in sorted(run_hours, reverse=True):
        cand = now.replace(hour=h, minute=0, second=0, microsecond=0)
        if cand > now:
            cand -= timedelta(days=1)
        if (now - cand).total_seconds()/3600 >= min_age_h:
            return cand
    h = sorted(run_hours)[-1]
    return (now - timedelta(days=1)).replace(hour=h, minute=0, second=0, microsecond=0)

