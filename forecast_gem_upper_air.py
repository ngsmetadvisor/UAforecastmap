def _decimate_stations(records, spacing_km=500):
    if not records:
        return records
    dlat = spacing_km / 111.32
    seen = {}
    out  = []
    for d in records:
        lat = d.get('lat')
        lon = d.get('lon')
        if lat is None or lon is None:
            continue
        dlon = spacing_km / (111.32 * np.cos(np.radians(lat)))
        cell = (int(lat / dlat), int(lon / dlon))
        if cell not in seen:
            seen[cell] = True
            out.append(d)
    return out

